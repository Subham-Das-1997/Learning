package basicprograms;
public class IfElseIf 
{
	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		int c=30;
		
		if(a==b)
		{
			System.out.println("1");
		}
		else if(b!=c)
		{
			System.out.println("2");
		}
		else if(c!=a)
		{
			System.out.println("3");
		}
		else
		{
			System.out.println("4");
		}
	}
}
